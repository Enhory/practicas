num1 = int(input("Favor de ingresar el primer número: "))
num2 = int(input("Favor de ingresar el segundo número: "))

print("Los dos números son iguales: " + str(num1 == num2))
print("Los dos números son diferentes: " + str(num1 != num2))
print("El primer número es mayor que el segundo: " + str(num1 >= num2))
print("El segundo número es mayor o igual que el primer número: " + str(num2 >= num1))



